int		ft_intlen(int n)
{
	int len;

	if (n < 0)
		n = n * -1;
	len = 1;
	if (n < 10 && n > 0)
		return (1);
	while (n > 9)
	{
		len++;
		n = n / 10;
	}
	return (len);
}
